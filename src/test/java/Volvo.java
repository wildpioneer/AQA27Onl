import java.util.Objects;

public class Volvo {

    private int id;
    private String name;
    private int weight;

    public Volvo(int id, String name, int weight) {
        this.id = id;
        this.name = name;
        this.weight = weight;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Volvo volvo = (Volvo) o;
        return weight == volvo.weight && Objects.equals(name, volvo.name);
    }

    @Override
    public int hashCode() {
        return Objects.hash(name, weight);
    }
}
